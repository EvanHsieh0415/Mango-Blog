<p>我們不得不分叉——因為許多核心資產是由 Lex 的公司 Forge LLC 授權的。我們分叉並重塑品牌會更好。我們將移除那些屬於 MinecraftForge 項目的資產，並在開發我們自己的品牌和風格。</p>
<p>需要注意的是，這還不是一次硬性分叉。我們目前仍然與現有的 Forge 兼容。未來我們是否會打破這種兼容性是許多討論的話題之一。一些人希望我們迅速清理技術債務，另一些人認為我們應該對這項工作採取更審慎的態度。可以說，這兩種觀點都有其道理，我們鼓勵討論和成人之間的辯論，探討在何種時間表上採取何種措施。</p>
