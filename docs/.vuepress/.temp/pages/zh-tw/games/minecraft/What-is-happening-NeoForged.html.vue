<h2 id="這裡發生了什麼事" tabindex="-1"><a class="header-anchor" href="#這裡發生了什麼事"><span>這裡發生了什麼事？</span></a></h2>
<p>自 2023 年 7 月 12 日起，原本稱為 “Forgecord” 的 Discord 項目重塑為一個新項目——NeoForged。</p>
<p>NeoForged 是 MinecraftForge 的一個分支。原始團隊幾乎全員加入了新團隊，除了顯而易見的一個例外，LexManos（以下簡稱 Lex）。</p>
<p>這篇公告將嘗試解釋導致這一重大行動的情況。它呈現了新團隊的觀點，具體來說，是我的（cpw）的觀點和動機。</p>
<h2 id="為什麼會發生這種情況" tabindex="-1"><a class="header-anchor" href="#為什麼會發生這種情況"><span>為什麼會發生這種情況？</span></a></h2>
<p>首先，我要說明清楚。將這歸因於單一事件就如同將千刀萬剮歸因於一刀。</p>
<p>在 Minecraft Forge Discord 最近的困難之後，我想了解為什麼 Curle 感覺需要辭去 Lex 給她的職位——即項目領導。她在升任領導之前做了一個驚人的工作，組織了這個團隊，對我來說，她在短短時間內感覺需要離開是令人驚訝的。與她的討論讓我意識到，她花了很多時間建立的團隊一直在持續遇到困難，而 Lex 是問題的根源。</p>
<p>我要說清楚，Lex 在模組社群中已經成為問題很多很多年了。幾乎每個老資格的 Minecraft 模組製作者在過去 12 年中都曾與他有過負面互動。當情況允許時，我曾試圖引導人們不要因為 Lex 而放棄 Forge——在很多私人對話中，我為他辯護，因為有沮喪的模組製作者被 Lex 第 1000 次稱作“婊子”，或者因為像討論 Fabric 或核心模組這樣的瑣碎違規行為而被我們的在線空間禁止。</p>
<p>在評估我所處的情況時，很快就清楚我可以採取以下三種行動之一：</p>
<ol>
<li>什麼也不做。如果我什麼也不做，我相信我們會看到 Forge 項目的慢慢解體。這就是為什麼我們最初招募 Curle 的原因——試圖幫助建立一個有用的團隊，使 Lex 能夠退出公眾視野和社群管理方面。她的離開會證明 Forge 團隊太過有毒，無法繼續以可行的方式運作，我的看法是如此。我們會看到更多的公開爭端和濫用行為，更多的模組製作者和社群領袖會變得越來越沮喪。</li>
<li>照顧 Lex。事實上，這是我在過去 10 年中一直在做的事，或多或少。他不會這麼看，但我一直在為 Lex 辯護，對抗所有來者。我真的以為如果他能停止在網上這麼有毒，我們會變得成功。</li>
<li>做我們已經做的事——分叉這個項目，重新開始。</li>
</ol>
<p>需要注意的是，由於個人身體和心理健康問題，我無法繼續做第 2 項，反而退出了社群——事實上是第 1 項——因為我需要保持健康。Lex 多次辯稱我有能力控制他，但這只是歷史的偶然，而不是我認為應該在兩個成年朋友之間必要的事情，如果這殺了我——這種可能性不是零（我不是年輕人）——那麼無論如何也不會有剩下的人。</p>
